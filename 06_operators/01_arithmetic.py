a = 12
b = 2

print("a + b = ",a + b)
print("a - b = ",a - b)
print("a / b = ",a / b)
print("a // b = ",a // b)   # floor division, answer without decimals
print("a % b = ",a % b)     # modulo.. remainder when a is divided by b
print("a * b = ",a * b)   
print("a ** b = ",a ** b)    # exponentitation, a to the power of b

